                ("representation", "How the workload is represented today"),
                ("algebraic_state", "Algebraic state and projected observation"),
                ("operator_structure", "Operator structure"),
                ("conservation_transfer", "Conservation and transfer structure"),
                ("memory_movement", "Memory and layout movement"),
                ("solver_path", "Solver and runtime path"),
                ("hardware_fit", "Hardware fit"),
                ("feasibility", "Technical feasibility"),
                ("recommendation", "What we would recommend proving next"),

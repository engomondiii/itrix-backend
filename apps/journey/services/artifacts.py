def _success_overview(thread) -> dict:
    """
    The State 10 standing summary, PINNED to the top of the thread.

    Regenerated on material change (§17.3). Carries no commercial content: this is not an
    upsell surface, and the overview is the most-read thing on it.
    """
    client = getattr(thread, "client", None)
    payload = {
        "title": "Welcome back. Here is where things stand.",
        "outcomes": [], "deployments": [], "open_support": 0,
        "next_review": None, "team": [],
    }
    if client is None:
        return payload

    try:
        from apps.customer_success.services import outcome_tracker, success_review
        from apps.customer_success.models import RelationshipTeamMember, SupportRequest

        payload["outcomes"] = [
            {"title": o.title, "status": o.status, "statusLabel": o.get_status_display()}
            for o in outcome_tracker.for_client(client)[:10]
        ]
        payload["open_support"] = SupportRequest.objects.filter(
            client=client, resolved_at__isnull=True
        ).count()
        review = success_review.upcoming(client)
        payload["next_review"] = review.scheduled_at.isoformat() if review else None
        payload["team"] = [
            {"name": m.display_name, "role": m.get_role_display(), "helpsWith": m.helps_with}
            for m in RelationshipTeamMember.objects.filter(client=client)
        ]
    except Exception:  # noqa: BLE001
        logger.debug("success overview partially unavailable")
    return payload


def regenerate_success_overview(client) -> None:
    """Rebuild the pinned overview on material change."""
    from apps.conversations.models import Thread

    thread = (
        Thread.objects.filter(client=client).order_by("-last_activity_at").first()
    )
    if thread is None:
        return
    try:
        generate(thread, ARTIFACT_SUCCESS_OVERVIEW, disclosure_level="customer_contract",
                 force=True)
    except Exception:  # noqa: BLE001
        logger.exception("success overview regeneration failed for client %s", client.id)


def for_thread(thread, *, ceiling: str = "public"):
    """
    Current artifacts on a thread, filtered to what the ceiling authorizes.

    Superseded versions are excluded — the customer sees what is true now, with older
    versions reachable only through the audit trail.
    """
    from apps.journey.constants import CEILING_RANK

    rank = CEILING_RANK.get(ceiling, 0)
    return [
        artifact
        for artifact in Artifact.objects.filter(thread=thread, superseded_by__isnull=True)
        if CEILING_RANK.get(artifact.disclosure_level, 0) <= rank
    ]

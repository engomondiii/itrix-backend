"""
Artifact generation, versioning and delivery (Backend v6.0 §3.3).

    An artifact is generated by a NAMED AGENT RUN, GOVERNED EXACTLY LIKE A MESSAGE, and
    stored with its disclosure_level and governance_status.

── THE STRUCTURAL SAFETY DECISIONS ──────────────────────────────────────────
Two artifact builders below deliberately CANNOT express something:

1. ``boundary_waste_map`` has NO NUMERIC FIELDS. Not "numbers are discouraged" — the
   payload has nowhere to put one. A performance claim before a PoC is therefore
   structurally impossible rather than merely against policy.

2. ``poc_evidence`` KPI outcomes are pass / partial / negative / pending, and nothing
   else. There is no "promising", no "trending positive", no percentage complete.
   Playbook §State 8 is unambiguous about why:

       A negative or partial result is reported as a negative or partial result. It is
       never re-described after the fact as a partial success, a learning, or a promising
       signal. THE CREDIBILITY OF EVERY FUTURE CLAIM DEPENDS ON THIS ONE.

── DELIVERY DOES NOT NAVIGATE ───────────────────────────────────────────────
``deliver()`` appends a reference to the thread and emits ``artifact.ready``. It never
returns a redirect and never sets a route. A frontend that responds to an artifact by
navigating is a defect (§11.9).
"""

from __future__ import annotations

import logging

from django.db import transaction

from apps.journey.constants import (
    ARTIFACT_BOUNDARY_WASTE_MAP,
    ARTIFACT_DOCUMENT,
    ARTIFACT_INTEGRATION_READINESS,
    ARTIFACT_PITCH_ROOM,
    ARTIFACT_POC_EVIDENCE,
    ARTIFACT_REFLECTION,
    ARTIFACT_REVIEW_SUMMARY,
    ARTIFACT_SUCCESS_OVERVIEW,
    ARTIFACT_TYPES,
    ARTIFACTS_BY_STATE,
)
from apps.journey.models import journey_number
from apps.journey.models_artifacts import Artifact

logger = logging.getLogger("itrix")

# The only KPI outcomes that exist. Adding a fifth would be how "partial" becomes
# "promising" — so the list is closed and a test asserts it.
KPI_OUTCOMES = ("pass", "partial", "negative", "pending")


class ArtifactNotAuthorized(Exception):
    """Raised when a state does not authorize this artifact type."""


def authorized_types(journey_state: int) -> tuple[str, ...]:
    return ARTIFACTS_BY_STATE.get(journey_state or 1, ())


def is_authorized(artifact_type: str, journey_state: int) -> bool:
    return artifact_type in authorized_types(journey_state)


@transaction.atomic
def generate(thread, artifact_type: str, *, payload: dict | None = None,
             disclosure_level: str = "public", agent_run_id: str = "",
             force: bool = False) -> Artifact:
    """
    Generate and deliver an artifact into ``thread``.

    Refuses a type the subject's state does not authorize. That check is here, not in the
    view, because artifacts are generated from several places (the stop rule, a reveal, a
    regeneration task) and each of them would otherwise need to remember.
    """
    if artifact_type not in ARTIFACT_TYPES:
        raise ValueError(f"Unknown artifact type {artifact_type!r}")

    state = _state_for(thread)
    if not force and not is_authorized(artifact_type, state):
        raise ArtifactNotAuthorized(
            f"State {state} does not authorize a {artifact_type} artifact."
        )

    body = payload if payload is not None else build_payload(thread, artifact_type)
    governed_body, status = _govern(body, artifact_type)

    previous = (
        Artifact.objects.filter(thread=thread, type=artifact_type, superseded_by__isnull=True)
        .order_by("-version")
        .first()
    )
    version = (previous.version + 1) if previous else 1

    artifact = Artifact.objects.create(
        thread=thread,
        type=artifact_type,
        version=version,
        payload=governed_body,
        disclosure_level=disclosure_level,
        governance_status=status,
        generated_by_run=str(agent_run_id or ""),
        pinned=(artifact_type == ARTIFACT_SUCCESS_OVERVIEW),
    )

    if previous is not None:
        # SUPERSEDE, never overwrite — the earlier version is what the customer read.
        previous.superseded_by = artifact
        previous.save(update_fields=["superseded_by", "updated_at"])

    logger.info("artifact.generate %s v%s on thread=%s", artifact_type, version, thread.id)
    deliver(artifact)
    return artifact


def _state_for(thread) -> int:
    lead = getattr(thread, "lead", None)
    if lead is None:
        return 1
    return journey_number(getattr(lead, "journey_state", None)) or 1


def _govern(payload: dict, artifact_type: str) -> tuple[dict, str]:
    """
    Run the governance pass over the artifact's text.

    An artifact is outbound content like any other (§Phase 2: the claim checker "governs
    artifacts and generated questions, not only messages"). Held artifacts keep their
    payload server-side; the serializer withholds it.
    """
    from apps.journey.models_artifacts import Artifact as _A

    text = _collect_text(payload)
    if not text:
        return payload, _A.GovernanceStatus.AUTO_APPROVED

    try:
        from apps.governance.services.claim_checker import check

        decision = check(text, claim_level=2, context="public")
        if decision.status == "auto_approved":
            return payload, _A.GovernanceStatus.AUTO_APPROVED
        if decision.status == "blocked":
            return payload, _A.GovernanceStatus.BLOCKED
        return payload, _A.GovernanceStatus.PENDING
    except Exception:  # noqa: BLE001
        logger.exception("artifact governance unavailable; holding")
        return payload, _A.GovernanceStatus.PENDING


def _collect_text(payload) -> str:
    """Flatten a payload's strings so governance sees everything the visitor would."""
    parts: list[str] = []

    def walk(node):
        if isinstance(node, str):
            parts.append(node)
        elif isinstance(node, dict):
            for value in node.values():
                walk(value)
        elif isinstance(node, (list, tuple)):
            for item in node:
                walk(item)

    walk(payload)
    return "\n".join(parts)


def deliver(artifact) -> None:
    """
    Append the artifact to its thread and emit ``artifact.ready``.

    Best-effort fan-out: the artifact is already persisted, so a socket failure delays
    its appearance rather than losing it.
    """
    try:
        from apps.conversations.services.fan_out import broadcast_artifact_ready

        broadcast_artifact_ready(artifact)
    except Exception:  # noqa: BLE001
        logger.debug("artifact.ready emit skipped (realtime unavailable)")


def bind_capability_token(artifact) -> str:
    """
    Mint a token bound to THIS ARTIFACT.

    Grants reach to the artifact's read endpoint ONLY — never the ability to post a turn
    to the thread that produced it (§3.3). That distinction is what makes an emailed
    brief safe to forward.
    """
    from apps.journey.services import capability_token as ct

    lead = getattr(artifact.thread, "lead", None)
    subject = str(getattr(lead, "id", "") or artifact.thread_id)
    token = ct.mint(sub=subject, typ=ct.TOKEN_CLIENT_PAGE, state=f"artifact:{artifact.id}")
    artifact.capability_token = token
    artifact.save(update_fields=["capability_token", "updated_at"])
    return token


